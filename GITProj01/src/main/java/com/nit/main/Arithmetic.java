package com.nit.main;

public class Arithmetic 
{
	public double sum(double x,double y)
	{
		//adding two numbers
		double z=0.0;
		z=x+y;
		return z;
	}
	
	public double sub(double x,double y)
	{
		//performing subtraction with third variable
		//performing subtraction using z
		double z=x-y;
		return z;
	}
}
